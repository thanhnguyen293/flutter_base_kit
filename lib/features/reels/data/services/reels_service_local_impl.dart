import 'package:flutter_base_kit/project_imports.dart';

class ReelsServiceLocalImpl implements ReelsService {
  int currentIndex = 0;
  final int pageSize = 10;

  Future<List<ReelEntity>> getVideos({ String? cursor}) async {
    final startIndex = currentIndex;
    final endIndex = startIndex + pageSize;
    currentIndex = endIndex;
    if (startIndex >= _mockReels.length) {
      return [];
    }
    if (endIndex > _mockReels.length) {
      return _mockReels.sublist(startIndex);
    }
    return _mockReels.sublist(startIndex, endIndex);
  }
}

